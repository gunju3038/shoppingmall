package com.ezen.haha.product;

public class ProductDTO {
	int snum, su, price, totprice, best;
	String sname, stype, color, ssize, image, intro, sideimage1, sideimage2, sideimage3;
	
	public ProductDTO() {}

	public int getSnum() {
		return snum;
	}

	public void setSnum(int snum) {
		this.snum = snum;
	}

	public int getSu() {
		return su;
	}

	public void setSu(int su) {
		this.su = su;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

	public String getStype() {
		return stype;
	}

	public void setStype(String stype) {
		this.stype = stype;
	}

	public String getSsize() {
		return ssize;
	}

	public void setSsize(String ssize) {
		this.ssize = ssize;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}

	public int getTotprice() {
		return totprice;
	}

	public void setTotprice(int totprice) {
		this.totprice = totprice;
	}

	public int getBest() {
		return best;
	}

	public void setBest(int best) {
		this.best = best;
	}

	public String getSideimage1() {
		return sideimage1;
	}

	public void setSideimage1(String sideimage1) {
		this.sideimage1 = sideimage1;
	}

	public String getSideimage2() {
		return sideimage2;
	}

	public void setSideimage2(String sideimage2) {
		this.sideimage2 = sideimage2;
	}

	public String getSideimage3() {
		return sideimage3;
	}

	public void setSideimage3(String sideimage3) {
		this.sideimage3 = sideimage3;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}
	
}
